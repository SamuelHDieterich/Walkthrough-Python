x = 6
y = 2

# Soma
print(f'{x}+{y} = {x + y}')
# 6+2 = 8

# Subtração
print(f'{x}-{y} = {x - y}')
# 6-2 = 4

# Multiplicação
print(f'{x}x{y} = {x * y}')
# 6x2 = 12

# Divisão
print(f'{x}/{y} = {x / y}') 
# 6/2 = 3.0
# Note que o resuldo será um float, mesmo o resultando sendo inteiro.

# Potência
print(f'{x}^{y} = {x ** y}') # Alternativamente: pow(x,y)
# 6^2 = 36

# Divisão inteira
print(f'Parte inteira de {x}/{y} é {x // y}')
# Parte inteira de 6/2 é 3

# Resto da divisão
print(f'Resto de {x}/{y} é {x % y}')
# Resto de 6/2 é 0